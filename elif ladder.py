## prb for print single digit using elif ladder (0 to 9)
n=int(input("enter the number"))
if (n==0):
    print(n,"zero")

elif(n==1):
    print(n,"one")

elif(n==2):
    print(n,"two")

elif(n==3):
    print(n,"three")

elif(n==4):
    print(n,"four")

elif(n==5):
    print(n,"five")

elif(n==6):
    print(n,"six")

elif(n==7):
    print(n,"seven")

elif(n==8):
    print(n,"Eight")

elif(n==9):
    print(n,"Nine")

else:
    print("invalid digit")

          
























    
